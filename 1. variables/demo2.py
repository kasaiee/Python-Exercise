# تعریف متغیر

x = 12
y = 6

print('x = '+str(x))
print('y = '+str(y))

# در پایتون شما میتوانید متغیر هاتون رو جایگزین کنید که جلو تر بیشتر راجع بهش میبینید

# برای جمع اعداد در پایتون از + استفاده میشود

total = x + y
print("x + y = "+str(total))

# برای تفریق اعداد از - استفاده میشود

total = x - y
print("x - y = "+str(total))

# برای ضرب اعداد از * اسفاده میشود
 
total = x * y
print("x * y = "+str(total))

# برای تقسیم اعداد از / استفاده میشود

total = x / y
print("x / y = "+str(total))


